"use strict";

// Klass för en produkt i JavaScript.
class Item {
    name;
    image;
    price;
    count; // Antal, hur många exemplar
    stripeProductId;

    constructor(name, image, price, count, stripeProductId) {
        this.name = name;
        this.image = image;
        this.price = price;
        this.count = count;
        this.stripeProductId = stripeProductId;
        this.stripeProductId = stripeProductId;
    }
}
